# Получаем предложение от пользователя
sentence = input("Введите предложение: ")

# Разбиваем предложение на слова, используя пробел как разделитель
words = sentence.split()

# Считаем количество слов в предложении
word_count = len(words)

# Выводим результат
print(f"Количество слов в предложении: {word_count}")